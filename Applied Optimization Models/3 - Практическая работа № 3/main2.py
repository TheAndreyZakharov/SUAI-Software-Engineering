from scipy.optimize import linprog

# Коэффициенты целевой функции
c = [1, 6]

# Матрица коэффициентов ограничений
A = [
    [1, 2],       # для x1 + 2x2 ≤ 10
    [-3, 3],      # для -3x1 + 3x2 ≤ -6 (преобразовано из 3x1 - 3x2 ≥ 6)
    [2, 3],       # для 2x1 + 3x2 ≤ 6
    [-3, -1]      # для -3x1 - x2 ≤ -4 (преобразовано из 3x1 + x2 ≥ 4)
]

# Вектор правых частей ограничений
b = [10, -6, 6, -4]

# Ограничения на переменные
x0_bounds = (0, None)  # x1 ≥ 0
x1_bounds = (0, None)  # x2 ≥ 0

# Решение задачи линейного программирования
res = linprog(c, A_ub=A, b_ub=b, bounds=[x0_bounds, x1_bounds], method='highs')

print(f'Оптимальное значение целевой функции: {res.fun}')
print(f'Оптимальные значения переменных: x1 = {res.x[0]}, x2 = {res.x[1]}')
