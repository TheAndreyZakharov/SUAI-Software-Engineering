import numpy as np
from scipy.optimize import linprog

# Коэффициенты целевой функции (отрицательные, так как мы минимизируем)
c = [-40, -70, -120, -120, -50]

# Матрица ограничений (левая сторона)
A = [
    [2, 3, 5, 4, 4],  # трудозатраты
    [2, 2, 4, 5, 0],  # металл
    [1, 3, 2, 0, 4],  # пластик
    [1, 2, 3, 3, 2]   # краска
]

# Вектор правых частей ограничений
b = [900, 8500, 4000, 5000]

# Ограничения на неотрицательность
bounds = [(0, None) for _ in range(len(c))]

# Решаем задачу
result = linprog(c, A_ub=A, b_ub=b, bounds=bounds)

if result.success:
    print("Оптимальное количество холодильников каждой марки:")
    for i, x in enumerate(result.x):
        print(f"Марка {i+1}: {x:.2f}")
    print(f"Максимальная прибыль: {-result.fun:.2f}")  # Умножаем на -1, так как мы минимизируем
else:
    print("Решение не найдено.")