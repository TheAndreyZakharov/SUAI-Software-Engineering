% Коэффициенты целевой функции (отрицательные, так как минимизируем)
c = [-40, -70, -120, -120, -50];

% Матрица ограничений (левая сторона)
A = [
    2, 3, 5, 4, 4;  % трудозатраты
    2, 2, 4, 5, 0;  % металл
    1, 3, 2, 0, 4;  % пластик
    1, 2, 3, 3, 2   % краска
];

% Вектор правых частей ограничений
b = [900, 8500, 4000, 5000];

% Ограничения на неотрицательность
lb = zeros(5,1);  % нижняя граница = 0 для всех переменных

% Решение задачи линейного программирования
[x, fval, exitflag] = linprog(c, A, b, [], [], lb, []);

% Проверка успешности решения
if exitflag == 1
    disp('Оптимальное количество холодильников каждой марки:');
    for i = 1:length(x)
        fprintf('Марка %d: %.2f\n', i, x(i));
    end
    fprintf('Максимальная прибыль: %.2f\n', -fval);  % Умножаем на -1, так как минимизировали
else
    disp('Решение не найдено.');
end