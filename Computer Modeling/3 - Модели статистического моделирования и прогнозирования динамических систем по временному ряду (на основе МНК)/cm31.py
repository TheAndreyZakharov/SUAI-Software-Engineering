import numpy as np

# Коэффициенты для системы уравнений
A = np.array([
    [140, 28, 7],
    [784, 140, 28],
    [4676, 784, 140]
])

# Правая часть системы
B = np.array([821124.2, 3298846.8, 16530009])

# Решение системы
solution = np.linalg.solve(A, B)

# Вывод результатов
a, b, c = solution
print(f"Решение системы: a = {a:.4f}, b = {b:.4f}, c = {c:.4f}")