.MODEL SMALL
.STACK 256
.DATA
MYTEXT DB 'abcdefghijklmnopqrstuvwxyz*,./*;ABCDEFGHIJKLMNOPQRSTUVWXYZ1234567890', 13, 10, '$'
length = ($ - MYTEXT) - 3
.CODE


CHANGE PROC NEAR
NOP
CMP AH,'d'
JB CHECK_S
CMP AH,'j'
JA DONE
SUB AH,20h
DONE:
MOV [BX],AH
RET
CHECK_S:
CMP AH,'s'
JNE DONE
MOV AH,'S'
JMP DONE

CHANGE ENDP


Start:
MOV AX,@DATA
MOV DS,AX
XOR AX,AX
LEA DX,MYTEXT
MOV AH,09h
INT 21h
MOV CX,length
LEA BX,MYTEXT

MT1:
MOV AH,[BX]
CALL CHANGE

MT2:
INC BX
LOOP MT1

LEA DX,MYTEXT
MOV AH,09h
INT 21h

NOP
MOV AX,4C00h
INT 21h

END Start
